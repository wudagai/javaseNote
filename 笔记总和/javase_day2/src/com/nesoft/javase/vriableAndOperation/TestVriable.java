package com.nesoft.javase.vriableAndOperation;

public class TestVriable {

	public static void main(String[] args) {
//		变量的声明
		int i;  //int :数据类型   i：变量
//		变量的赋值
		i=10;
		System.out.println(i);//访问变量
		i=20;
		System.out.println("i:"+i);
//		变量的初始化(变量的第一次赋值，一般是变量声明的同时完成初始化)
		int j=20; 
		System.out.println("j:"+j);
		j=30;
		System.out.println("j:"+j);
//		变量必须初始化之后才能使用
		int m;
//		System.out.println(m);
		m=20;
		System.out.println(m);
		
	}

}
